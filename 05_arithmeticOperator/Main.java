public class Main {
    public static void main(String[] args) {

        // Arithmetic Operators

        int x = 6;
        int y = 7;
        int z;

        // z = x + y;
        // z = x - y;
        // z = x * y;
        // z = x + y;
        z = x % y; // Modulus Operator // gives remainder

        // Assignment Operator
        z = 10;
        z += 5;
        System.out.println(z);

        // Increment and Decrement Operators
        z++;
        System.out.print(z);

        z--;
        System.out.print(z);

        // Order of Operations [P-E-M-D-A-S]
    }
}